    public void lerCliente(Cliente c, Clientes cl) {
        this.cliente = c;
        nome.setText(c.getNome());
        inputCpf.setText(cliente.getCpf());
        rg.setText(String.valueOf(cliente.getRg()));
        dataNasc.setText(cliente.getDataNasc());
        email.setText(cliente.getEmail());
        celular.setText(cliente.getCelular());
        cep.setText(cliente.getCep());
        cidade.setText(cliente.getCidade());
        endereco.setText(cliente.getEndereco());
        complemento.setText(cliente.getComplemento());
        boxUf.setSelectedItem(cliente.getEstado());
        this.clientes = cl;
    }

